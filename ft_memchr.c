#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char *st;

	st = (char*)s;
	while (n-- > 1 && *st != ((char)c))
		st++;
	if (*st == ((char)c))
		return (st);
	return (0);
}
