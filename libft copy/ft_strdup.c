#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		j;
	int		i;
	char	*str;

	j = 0;
	while (s1[j])
		j++;
	str = (char*)malloc(sizeof(*str) * (j + 1));
	if (str == NULL)
		return (0);
	i = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
