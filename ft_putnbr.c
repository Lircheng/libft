#include "libft.h"

void	ft_putnbr(int n)
{
	size_t nbr;

	if (n < 0)
	{
		ft_putchar('-');
		nbr = (size_t)n * -1;
	}
	else
		nbr = (size_t)n;
	if (nbr > 9)
	{
		ft_putnbr(nbr / 10);
		ft_putnbr(nbr % 10);
	}
	else
		ft_putchar(nbr + '0');
}
