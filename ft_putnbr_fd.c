#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	size_t nbr;

	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		nbr = (size_t)n * -1;
	}
	else
		nbr = (size_t)n;
	if (nbr > 9)
	{
		ft_putnbr_fd(nbr / 10, fd);
		ft_putnbr_fd(nbr % 10, fd);
	}
	else
		ft_putchar_fd(nbr + '0', fd);
}
